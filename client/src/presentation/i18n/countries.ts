export function getCountryNameByCode(code: string) {
  const langToFind = Object.values(LANGUAGES).includes(i18next.language)
    ? i18next.language
    : FALLBACK_LNG;
  const country = COUNTRIES.find((c) => c.code === code);
  if (country) {
    return country.name[langToFind];
  }
  return null;
}
